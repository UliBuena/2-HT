cor = input("Insira a cor do produto para ver o valor:\nCores: verde, azul, amarelo, vermelho\n")

if cor == "verde":
  print("O preço do produto é: R$10")

elif cor == "azul":
  print("O preço do produto é: R$20")

elif cor == "amarelo":
  print("O preço do produto é: R$30")

elif cor == "vermelho":
  print("O preço do produto é: R$40")

else:
  print("A cor inserida não está em nosso catálogo")